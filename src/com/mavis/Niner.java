package com.mavis;

public class Niner {

	public static void main(String[] args) {		
		for(int i=1; i<=4; i++){
			System.out.println("2 x "+i + " =  " + (2*i));
			}
		for(int k=5; k<=9; k++)
			System.out.println("2 x "+k + " = " + (2*k));
		
		for(int h = 1; h <= 9; h++){
			if((3*h) < 10){
				System.out.println("3 x "+h + " =  " + (3*h));
			}else{
				System.out.println("3 x "+h + " = " + (3*h));
			}
		}
	}

}
